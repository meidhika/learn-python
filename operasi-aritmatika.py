#operasi pengurangan -
a = 10
b= 3
hasil = a - b
print("hasil pengurangan : ", a, "-", b, "=", hasil)

#operasi perkalian *
a = 10
b= 3
hasil = a * b
print("hasil perkalian : ", a, "*", b, "=", hasil)

#operasi pembagian /
